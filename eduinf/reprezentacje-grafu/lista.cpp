#include <iostream>
#include <vector>
using namespace std;

int main() {
	////////////////////////////////////////////////////////////////////////////
	//	POBIERANIE DANYCH
	////////////////////////////////////////////////////////////////////////////
	int v;
	cin >> v;
	vector<vector<int>> tab(v);
	while (true) {
		int a, b;
		cin >> a >> b;
		if (!cin) break;
		tab[a].push_back(b);
	}
	
	////////////////////////////////////////////////////////////////////////////
	//	RYSOWANIE GRAFU SKIEROWANEGO
	////////////////////////////////////////////////////////////////////////////
	for (int i = 0; i < v; i++) {
		cout << i << ":";
		for (auto x : tab[i]) {
			cout << " " << x;
		}
		cout << endl;
	}
	
	return 0;
}